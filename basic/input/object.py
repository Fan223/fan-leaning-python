print(bool(False))  # False
print(bool(0))  # 整数 0
print(bool(0.0))  # 浮点数 0.0
print(bool(None))  # 空
print(bool(''))  # 空串
print(bool([]))  # 空列表
print(bool(list()))  # 空列表
print(bool(()))  # 空元组
print(bool(tuple()))  # 空元组
print(bool({}))  # 空字典
print(bool(dict()))  # 空字典
print(bool(set()))  # 空集合
